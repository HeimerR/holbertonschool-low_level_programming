#include "3-calc.h"
/**
* op_add - Adds 2 numbers
*
* @a: number 1
* @b: number 2
* Return: sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - substract 2 numbers
*
* @a: number 1
* @b: number 2
* Return: subs
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - multiplies 2 numbers
*
* @a: number 1
* @b: number 2
* Return: mul
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div - divides 2 numbers
*
* @a: number 1
* @b: number 2
* Return: division
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - Struct op
*
* @a: number 1
* @b: number 2
* Return: modulo
*/
int op_mod(int a, int b)
{
return (a % b);
}
